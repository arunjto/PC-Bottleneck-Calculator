export interface CPU {
  id: string;
  name: string;
  brand: 'Intel' | 'AMD';
  series: string;
  baseClock: number; // GHz
  boostClock: number; // GHz
  cores: number;
  threads: number;
  tdp: number; // Watts
  benchmarkScore: number; // Relative performance score
  releaseYear: number;
  launchPrice: number; // USD
  currentPrice: number; // USD
  tier: 'High-End' | 'Mid-Range' | 'Budget' | 'Entry-Level';
  category: 'Desktop' | 'Mobile' | 'Workstation' | 'Server';
  imageUrl: string;
  socket?: string;
  architecture: string;
  officialUrl?: string; // Official manufacturer product page URL
}

export interface GPU {
  id: string;
  name: string;
  brand: 'NVIDIA' | 'AMD';
  series: string;
  vram: number; // GB
  baseClock: number; // MHz
  boostClock: number; // MHz
  cudaCores?: number; // NVIDIA only
  streamProcessors?: number; // AMD only
  tdp: number; // Watts
  benchmarkScore: number; // Relative performance score
  releaseYear: number;
  launchPrice: number; // USD
  currentPrice: number; // USD
  tier: 'High-End' | 'Mid-Range' | 'Budget' | 'Entry-Level';
  category: 'Desktop' | 'Mobile' | 'Workstation';
  imageUrl: string;
  architecture: string;
  officialUrl?: string; // Official manufacturer product page URL
}

export interface Game {
  id: string;
  name: string;
  releaseYear: number;
  category: 'AAA' | 'Esports' | 'Indie' | 'Simulation' | 'Sandbox' | 'Survival';
  cpuDemand: 'Low' | 'Medium' | 'High' | 'Extreme';
  gpuDemand: 'Low' | 'Medium' | 'High' | 'Extreme';
  ramRequirement: number; // GB
  storageRequirement: number; // GB
  optimizations: string[]; // DLSS, FSR, etc.
}
